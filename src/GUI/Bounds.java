package GUI;

/**
 * Custom bounds class, used for the button bounds in EditorPanel.
 *
 * @author FlashYoshi
 */
public class Bounds {

    public int x;
    public int y;
    public int width;
    public int height;

    public Bounds(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
